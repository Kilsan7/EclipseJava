package com.javalec.ifEx;

public class IfEx {
	public static void main(String[] args) {
		
		int i = 10;
		int j = 20;
		int h = 210;
		
		if(i > j) {
			System.out.println("i가 j보다 큽니다.");
		}
		
		if(i == h) {
			System.out.println("i와 h는 같습니다.");
		}
		else {
			System.out.println("i와 h 값은 다릅니다.");
		}
		
	}
}
